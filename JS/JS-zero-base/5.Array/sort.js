let arr = [9, 5, 7, 1, 4];

console.log(arr.sort()); //[ 1, 4, 5, 7, 9 ]
console.log(arr.reverse()); //[ 9, 7, 5, 4, 1 ]

let arr_2 = ['orange', 'banana', 'apple', 'melon'];

console.log(arr_2.sort()); //[ 'apple', 'banana', 'melon', 'orange' ]
console.log(arr_2.reverse()); //[ 'orange', 'melon', 'banana', 'apple' ]
